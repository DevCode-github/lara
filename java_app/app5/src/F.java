class F 
{
	public static void main(String[] args) 
	{
		System.out.println(Integer.toBinaryString(288));
		System.out.println(288 << 1);
		System.out.println(288 << 2);
		System.out.println(288 << 3);
	}
}
/*
288		 ==>   100100000
288 << 1 ==>  1001000000  
288 << 1 ==> 10010000000 
288 << 1 ==>100100000000 
*/